public class solveNQueens {
    public List<List<String>> solveNQueens(int n) {
        List<List<String>> res=new ArrayList<List<String>>();
        for (int i=0;i<n*n;i++){
            String[][] plat=new String[n][n];
            Arrays.fill(plat,"Q");


        }

        return  res;
    }
}
